public class latihan_overloding {
    private Integer hasil, x, y;
    public void setx(int X)
    {
        this.x=X;
    }
    public Integer getx()
    {
        return x;
    }
    public void sety(int Y)
    {
        this.y=Y;
    }
    public Integer gety()
    {
        return y;
    }
    Integer penjumlahan()
    {
        return hasil = x+y;
    }
    Integer penjumlahan(int a, int b)
    {
       return hasil = a + b;
    }
}
